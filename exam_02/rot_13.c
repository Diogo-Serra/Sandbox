#include <unistd.h>

void	rot_13(char *str)
{
	char	c;

	while (*str)
	{
		c = *str;
		if (c >= 'a' && c <= 'm')
			c += 13;
		else if (c >= 'n' && c <= 'z')
			c -= 13;
		else if (c >= 'A' && c <= 'M')
			c += 13;
		else if (c >= 'N' && c <= 'Z')
			c -= 13;
		write(1, &c, 1);
		str++;
	}
	write(1, "\n", 1);
}
