#include <unistd.h>

void	rotone(char *str)
{
	char	c;

	while (*str)
	{
		c = *str;
		if ((c >= 'a' && c <= 'y') || (c >= 'A' && c <= 'Y'))
			c++;
		else if (c == 'z' || c == 'Z')
			c -= 25;
		write(1, &c, 1);
		str++;
	}
	write(1, "\n", 1);
}
